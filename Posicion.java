
public class Posicion {
	private int x  ; 
	private int y ;
	public Posicion(int x , int y ){
			this.x = x  ; 
			this.y = y ; 		
	}
	private int getX() {
		return x;
	}
	private void setX(int x) {
		this.x = x;
	}
	private int getY() {
		return y;
	}
	private void setY(int y) {
		this.y = y;
	} 
	
}
